numero1 = int(input('Digite um número para calculo: '))
numero2 = int(input("Digite outro número para calculo: "))
soma = numero1 + numero2
subtracao = numero1 - numero2
multiplicacao = numero1 * numero2
divisao = numero1 // numero2

print( 'O resultado da soma é {} \n O resultado da subritração é {} \n O resultado da multiplicação é {} \n O resultado da divisão é {}'.format(soma, subtracao,multiplicacao,divisao))